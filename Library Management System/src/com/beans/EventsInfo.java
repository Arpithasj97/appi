package com.beans;


public class EventsInfo {
	private String e_Name;
	private String date;
	private String e_MName;
	private long e_ContactNo;
	private String e_Email;
	private String venue;
	
	
	public String getE_Name() {
		return e_Name;
	}
	public void setE_Name(String e_Name) {
		this.e_Name = e_Name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getE_MName() {
		return e_MName;
	}
	public void setE_MName(String e_MName) {
		this.e_MName = e_MName;
	}
	public long getE_ContactNo() {
		return e_ContactNo;
	}
	public void setE_ContactNo(long e_ContactNo) {
		this.e_ContactNo = e_ContactNo;
	}
	public String getE_Email() {
		return e_Email;
	}
	public void setE_Email(String e_Email) {
		this.e_Email = e_Email;
	}
	public String getVenue() {
		return venue;
	}
	public void setVenue(String venue) {
		this.venue = venue;
	}
	
}
